// ── таблицы ─────────────────────────────────────────────────────────────────
const BD = { style: BorderStyle.SINGLE, size: 4, color: "000000" };
const BORDERS = { top: BD, bottom: BD, left: BD, right: BD,
  insideHorizontal: BD, insideVertical: BD };

function cell(text, w, opts = {}) {
  const paras = (Array.isArray(text) ? text : [text]).map((t) =>
    new Paragraph({
      alignment: opts.center ? AlignmentType.CENTER : AlignmentType.LEFT,
      spacing: { before: 0, after: 0, line: 240 },
      children: [run(String(t), { size: 24, bold: opts.bold || false })],
    })
  );
  return new TableCell({
    width: { size: w, type: WidthType.DXA },
    margins: { top: 40, bottom: 40, left: 80, right: 80 },
    verticalAlign: VerticalAlign.CENTER,
    shading: opts.head ? { fill: "EEEEEE", type: ShadingType.CLEAR, color: "auto" } : undefined,
    children: paras,
  });
}

function makeTable(widths, headers, rows) {
  const headRow = new TableRow({
    tableHeader: true,
    children: headers.map((h, i) => cell(h, widths[i], { head: true, bold: true, center: true })),
  });
  const bodyRows = rows.map((r) =>
    new TableRow({ children: r.map((c, i) => cell(c, widths[i], {})) })
  );
  return new Table({
    width: { size: CW, type: WidthType.DXA },
    columnWidths: widths,
    borders: BORDERS,
    rows: [headRow, ...bodyRows],
  });
}

