// нумерованный список (каждый вызов new ref не нужен — один сквозной список в пределах группы)
let numRefCounter = 0;
function numberedList(items) {
  const ref = "num" + (numRefCounter++);
  return items.map((t) =>
    new Paragraph({
      numbering: { reference: ref, level: 0 },
      alignment: AlignmentType.JUSTIFIED,
      spacing: { line: LINE15, before: 0, after: 0 },
      children: [run(t)],
    })
  );
}

// формула: по центру, номер справа (безрамочная таблица), пустые строки до/после
const NO_BORDERS = {
  top: { style: BorderStyle.NONE }, bottom: { style: BorderStyle.NONE },
  left: { style: BorderStyle.NONE }, right: { style: BorderStyle.NONE },
  insideHorizontal: { style: BorderStyle.NONE }, insideVertical: { style: BorderStyle.NONE },
};
function formula(text, num) {
  const fCell = new TableCell({
    width: { size: CW - 1000, type: WidthType.DXA }, borders: NO_BORDERS,
    verticalAlign: VerticalAlign.CENTER,
    children: [new Paragraph({ alignment: AlignmentType.CENTER, spacing: { line: LINE15 },
      children: [run(text, { italics: true })] })],
  });
  const nCell = new TableCell({
    width: { size: 1000, type: WidthType.DXA }, borders: NO_BORDERS,
    verticalAlign: VerticalAlign.CENTER,
    children: [new Paragraph({ alignment: AlignmentType.RIGHT, spacing: { line: LINE15 },
      children: [run("(" + num + ")")] })],
  });
  return [
    new Paragraph({ spacing: { line: LINE15 }, children: [run("")] }),
    new Table({ width: { size: CW, type: WidthType.DXA }, columnWidths: [CW - 1000, 1000],
      borders: NO_BORDERS, rows: [new TableRow({ children: [fCell, nCell] })] }),
    new Paragraph({ spacing: { line: LINE15 }, children: [run("")] }),
  ];
}
