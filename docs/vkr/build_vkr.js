function tocLine(title, page, level) {
  return new Paragraph({
    tabStops: [{ type: TabStopType.right, position: 9000, leader: LeaderType.DOT }],
    spacing: { line: 276, before: 0, after: 40 },
    alignment: AlignmentType.LEFT,
    children: [
      new TextRun({ text: title + "\t" + (page + PAGE_SHIFT), font: "Times New Roman", size: 28, bold: level === 1 }),
    ],
  });
}

// ── единицы и константы оформления ──────────────────────────────────────────
const FONT = "Times New Roman";
const MONO = "Courier New";
const CW = 9638;             // ширина текста (A4: 11906 − 1701 − 567)
const IND = 709;             // абзацный отступ 1,25 см
const MM = 56.7;             // 1 мм в twips
const LINE15 = 360;          // полуторный интервал

// ── вспомогательные конструкторы абзацев ────────────────────────────────────
function run(text, o = {}) {
  return new TextRun({ text, font: o.font || FONT, size: o.size || 28,
    bold: o.bold || false, italics: o.italics || false });
}

// обычный текст: TNR 14, по ширине, 1,5, отступ первой строки 1,25
function body(text) {
  const children = Array.isArray(text) ? text : [run(text)];
  return new Paragraph({
    children,
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: LINE15, before: 0, after: 0 },
    indent: { firstLine: IND },
  });
}

// заголовок 1 уровня (нумерованный раздел): 18 пт, ПРОПИСНЫЕ, слева, с новой страницы
